<?php

$a = 2;
$b = "2";

//sama dengan setelah konversi data
var_dump($a == $b);

//sama dengan identik (tipe data dan nilainya sama)
var_dump($a === $b);

//tidak sama dengan stelah konversi data
var_dump($a != $b);
var_dump($a <> $b);

//tidak sama dengan identik
var_dump($a !== $b);

//lebih dari
var_dump(10 > 9);

//lebih dari sama dengan
var_dump(10 >= 9);

//kurang dari
var_dump(6 < 7);

//kurang dari sama dengan
var_dump(7 <= 7);



?>